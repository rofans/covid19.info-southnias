      <h3>What are Coronaviruses and what is COVID-19?</h3>
      <p>Coronaviruses (CoV) are a large family of viruses causing illnesses ranging from the common cold to pneumonia (a more severe lung infection).
      COVID-19 is the infectious disease caused by a strain of coronavirus, SARS-CoV-2.
      It was declared a pandemic by the World Health Organization in March 2020 and has spread globally.</p>
  
      <h3>How does COVID-19 spread?</h3>
      <p>Current evidence suggests that transmission of COVID-19 occurs primarily through the respiratory droplets of infected people, which are expelled when an infected person coughs, sneezes, talks or sings.
      These respiratory droplets can reach the eyes, nose or mouth of a susceptible person directly or indirectly (via contaminated surfaces), resulting in infection.</p>
      <p>Airborne transmission of the virus can occur in health care settings where aerosol-generating procedures are performed.
      While there have been limited reports of airborne transmission outside of health care settings internationally, its role and extent are under further study.
      MOH will continue to monitor the evidence as it emerges.</p>
      <p>To reduce the risk of spread, members of public must wear a mask when in indoor settings outside the home and observe safe distancing in mask-off settings.
      Individuals should also observe good hand hygiene and avoid crowded spaces if possible.</p>
   
      <h3>What are the symptoms of COVID-19?</h3>
      <p>The symptoms of COVID-19 infection are similar to that of an acute respiratory infection or pneumonia.
      These symptoms include fever, cough, runny nose, sore throat, loss of taste or smell.</p>

      <h3>What is the treatment for COVID-19?</h3>
      <p>COVID-19 is generally managed with supportive treatment provided based on the patient’s clinical condition.
      Treatments and drugs for COVID-19 continue to be monitored, and recommendations may further evolve over time.</p>

      <h3>Is the disease deadly? How is it compared to SARS?</h3>
      <p>Internationally, COVID-19 has been observed to cause severe disease and death in 2% to 3% of people with the infection, especially among the elderly and those with underlying health problems or compromised immune systems.
      Singapore’s mortality rate remains below the global average.</p>
